const eventMap = new Map(recentEvents.map((event) => [event.id, event] as const));

function getEvent(eventId: string) {
  const event = eventMap.get(eventId);

  if (!event) {
    throw new Error(`Missing mock event: ${eventId}`);
  }

  return event;
}

function eventToken(eventId: string, language: "en" | "pt-BR") {
  return createNoteEventToken(resolveLocalizedText(getEvent(eventId).name, language));
}

function buildNoteBody(
  eventIds: string[],
  en: string,
  ptBR: string,
): LocalizedText {
  const enPrefix = eventIds.map((eventId) => eventToken(eventId, "en")).join(" ");
  const ptPrefix = eventIds.map((eventId) => eventToken(eventId, "pt-BR")).join(" ");

  return {
    en: [enPrefix, en].filter(Boolean).join(" "),
    "pt-BR": [ptPrefix, ptBR].filter(Boolean).join(" "),
  };
}

type EncounterSeed = {
  access?: Encounter["access"];
  avatarUrl: string;
  company: string;
  connected: boolean;
  connectionCount: number;
  context: LocalizedText;
  description: LocalizedText;
  distance: LocalizedText;
  encounteredAtValue?: number;
  eventId: string;
  eventRole?: EventRole;
  followUp: FollowUpState;
  gradient: [string, string];
  homeVisible?: boolean;
  id: string;
  initials: string;
  interactionIcon?: InteractionIcon;
  interactionLabel?: LocalizedText;
  mutualConnectionIds: string[];
  name: string;
  note: LocalizedText;
  radar?: Encounter["radar"];
  role: LocalizedText;
  sharedEventIds: string[];
  socials: SocialLink[];
  tags: LocalizedText[];
  timeAgo: LocalizedText;
  verified?: boolean;
};

function encounter(seed: EncounterSeed): Encounter {
  const event = getEvent(seed.eventId);

  return {
    access: seed.access ?? "full",
    avatarUrl: seed.avatarUrl,
    company: seed.company,
    connected: seed.connected,
    connectionCount: seed.connectionCount,
    context: seed.context,
    description: seed.description,
    distance: seed.distance,
    encounteredAtValue: seed.encounteredAtValue,
    event: event.name,
    eventId: seed.eventId,
    eventRole: seed.eventRole ?? "attendee",
    eventTone: event.tone,
    followUp: seed.followUp,
    gradient: seed.gradient,
    homeVisible: seed.homeVisible ?? true,
    id: seed.id,
    initials: seed.initials,
    interactionIcon: seed.interactionIcon,
    interactionLabel: seed.interactionLabel,
    mutualConnectionIds: seed.mutualConnectionIds,
    name: seed.name,
    note: seed.note,
    radar: seed.radar,
    role: seed.role,
    sharedEventIds: seed.sharedEventIds,
    socials: seed.socials,
    tags: seed.tags,
    timeAgo: seed.timeAgo,
    verified: seed.verified ?? true,
  };
}

